    $(document).ready(() => {
        $.ajax({
            url: url_base + "partenaire.json.php",
            type: "GET",
            data: null,
            success: (resultat) => {
                var ul = $('#partenaires');
                ul.html("");
                var k = resultat.length - 1;
                for (let index = 0; index < resultat.length; index++) {
                    if (index < 5) {
                        var li = " ";
                        li += `<li class="list-group-item"> ${resultat[k].nompartenaire} </li>`
                        ul.append(li);
                        k--;
                    }
                }
            }
        })
    })
